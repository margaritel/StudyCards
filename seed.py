def seed():
    with app.app_context():
        # Проверяем — если наборы уже есть, не добавляем
        if CardSet.query.first():
            print('База уже заполнена. Пропускаем.')
            return

        print('Создаём тестового пользователя...')
        # Берём существующего или создаём нового
        user = User.query.filter_by(username='demo_user').first()
        if not user:
            password_hash = bcrypt.hashpw('demo123'.encode('utf-8'), bcrypt.gensalt()).decode('utf-8')
            user = User(username='demo_user', password_hash=password_hash)
            db.session.add(user)
            db.session.flush()

        print(f'Добавляем {len(SEED_DATA)} наборов карточек...')
        for i, data in enumerate(SEED_DATA):
            card_set = CardSet(
                title=data['title'],
                subject=data['subject'],
                grade=data['grade'],
                author_id=user.id,
                likes_count=len(data['cards']) * 2  # немного лайков для красоты
            )
            db.session.add(card_set)
            db.session.flush()

            for question, answer in data['cards']:
                card = Card(
                    set_id=card_set.id,
                    question=question,
                    answer=answer
                )
                db.session.add(card)

            print(f'  ✓ {data["title"]} ({len(data["cards"])} карточек)')

        db.session.commit()
        print('\nГотово! Тестовые данные добавлены.')
        print('Логин: demo_user')
        print('Пароль: demo123')


if __name__ == '__main__':
    seed()
